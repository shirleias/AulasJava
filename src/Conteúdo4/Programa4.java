package Conteúdo4;

public class Programa4 {
    public static void main(String[] args) {
        int i = 0;
        while (i <= 31){
            System.out.println("Número: " + i);
            i++;

        }

        for (int j = 0; j <= 31; j++ ){
            System.out.println("Número: " + j);
        }

    }//  i = i + 1 = i++
}
